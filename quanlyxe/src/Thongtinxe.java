    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Soxe = txtSoxe.getText();
        Hieuxe = txtHieuxe.getText();
        try {
            Soghe = Integer.parseInt(txtSoghe.getText());
        } catch (NumberFormatException e) {
            lbMes.setText("SO GHE PHAI LA SO NGUYEN");
            txtSoghe.requestFocus();
            return;
        }
        MaCN =  (String) cbMaCN.getSelectedItem();
        if (Soxe.equals("") || Hieuxe.equals("") || Soghe < 1 || MaCN.equals("")) {
            lbMes.setText("yeu cau dien day du thong tin");
            return;
        }
        if (edit == 0) {
            int m = JOptionPane.showConfirmDialog(this, "ban co muon luu hay khong", "save", JOptionPane.YES_NO_OPTION);
            if (m == 0) {
               
                try {
                    cnDB=new Connect_Var();
                    String insertsql="insert into Xe values('"+Soxe+"','"+Hieuxe+"',"+Soghe+",'"+MaCN+"')";
                    cnDB.SQL(insertsql);
                    vt = new Vector();
                    vt.add(Soxe);
                    vt.add(Hieuxe);
                    vt.add(Soghe);
                    vt.add(MaCN);
                    mtable.addRow(vt);
                    JOptionPane.showMessageDialog(this, "ban da luu thanh cong");
                } catch (Exception ex) {
                    Logger.getLogger(Thongtinxe.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        } else {
            int m = JOptionPane.showConfirmDialog(this, "ban chac chan muon thay doi", "save", JOptionPane.YES_NO_OPTION);
            if (m == 0) {
                try {
                    cnDB=new Connect_Var();
                    String updatesql="update Xe set MaCN='"+MaCN+"',Hieuxe='"+Hieuxe+"',Sochongoi="+Soghe+" where Soxe='"+Soxe+"'";
                    cnDB.SQL(updatesql);
                    mtable.setValueAt(Soxe, n, 0);
                    mtable.setValueAt(Hieuxe, n, 1);
                    mtable.setValueAt( Soghe, n, 2);
                    mtable.setValueAt(MaCN, n, 3);
                    JOptionPane.showMessageDialog(this, "ban da luu thanh cong");
                } catch (Exception ex) {
                    Logger.getLogger(Thongtinxe.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }

        lbMes.setText("");
        txtSoxe.setEnabled(false);
        txtHieuxe.setEnabled(false);
        txtSoghe.setEnabled(false);
        cbMaCN.setEnabled(false);
        btnNew.setEnabled(true);
        btnSave.setEnabled(false);
        btnEdit.setEnabled(true);
    }//GEN-LAST:event_btnSaveActionPerformed

