    private void Initcbbox()
    {
            try {
            cnDB = new Connect_Var();
            rs = cnDB.s.executeQuery("select * from ChiNhanh");
            rsmd = rs.getMetaData();
            if (rs != null) {
                 model=new DefaultComboBoxModel();
                while (rs.next()) {
                   
                    model.addElement(rs.getString(1));
                }
                rs.close();
                cnDB.s.close();
                cnDB.cn.close();
            }
        } catch (Exception ex) {
            Logger.getLogger(Thongtinxe.class.getName()).log(Level.SEVERE, null, ex);
        }
        cbMaCN.setModel(model);
    }
    private void InitTable(){
        vt = new Vector();
        vt.add("Soxe");
        vt.add("Hieuxe");
        vt.add("Chongoi");
        vt.add("MaCN");
        mtable = new DefaultTableModel(vt,0);
        //tXe.setModel(mtable);
        try {
            cnDB = new Connect_Var();
            rs = cnDB.s.executeQuery("select * from Xe");
            rsmd = rs.getMetaData();
            if (rs != null) {
                while (rs.next()) {
                    vt = new Vector();
                    vt.add(rs.getString(1).toString());
                    vt.add(rs.getString(2));
                    vt.add(rs.getInt(3));
                    vt.add(rs.getString(4));
                    mtable.addRow(vt);
                }
                rs.close();
                cnDB.s.close();
                cnDB.cn.close();
            }
        } catch (Exception ex) {
            Logger.getLogger(Thongtinxe.class.getName()).log(Level.SEVERE, null, ex);
        }
        tXe.setModel(mtable);
    }

