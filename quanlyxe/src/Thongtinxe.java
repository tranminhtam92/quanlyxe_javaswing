    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        while (mtable.getRowCount() >0) 
            mtable.removeRow(0);
        if (rbSoxe.isSelected()) {
            try {
                cnDB = new Connect_Var();
                rs = cnDB.s.executeQuery("select* from Xe where Soxe like '%" + txtSearch.getText() + "%' ");

            } catch (Exception ex) {
                Logger.getLogger(Thongtinxe.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            try {
                cnDB = new Connect_Var();
                rs = cnDB.s.executeQuery("select* from Xe where Hieuxe like '%" + txtSearch.getText() + "%' ");
            } catch (Exception ex) {
                Logger.getLogger(Thongtinxe.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        try {
            rsmd = rs.getMetaData();
            if (rs != null) {
                while (rs.next()) {
                    vt = new Vector();
                    vt.add(rs.getString(1));
                    vt.add(rs.getString(2));
                    vt.add(rs.getInt(3));
                    vt.add(rs.getString(4));
                    mtable.addRow(vt);
                }
                rs.close();
                cnDB.s.close();
                cnDB.cn.close();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Thongtinxe.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtSearchKeyReleased

